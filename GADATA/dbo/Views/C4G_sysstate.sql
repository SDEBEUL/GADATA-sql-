CREATE VIEW dbo.C4G_sysstate
AS
SELECT top 1000        *, ROW_NUMBER() OVER (PARTITION BY x.controller_id ORDER BY x._timestamp DESC) AS rnDESC
FROM            (/*data from rt_sys_event table. */ SELECT rt_sys_event.id, rt_sys_event.controller_id, rt_sys_event._timestamp, rt_sys_event.sys_state, 
                                                    robotState = dbo.fn_robstate(rt_sys_event.sys_state)
                          /*calculates a robot state a running robot has 2 a non running one 0 */ FROM GADATA.dbo.rt_sys_event AS rt_sys_event
                          WHERE        rt_sys_event._timestamp BETWEEN GETDATE() - 1 AND GETDATE()
                          /*data from L_operation (to catch robots goning offline)*/ UNION
                          SELECT        l_operation.id, l_operation.controller_id, l_operation._timestamp, sys_state = 262144, robotState = 0
                          FROM            GADATA.dbo.l_operation AS l_operation
                          WHERE        (l_operation._timestamp BETWEEN GETDATE() - 1 AND GETDATE()) AND (l_operation.code = 4)/*connection lost */ ) AS x
order by _timestamp desc
GO
