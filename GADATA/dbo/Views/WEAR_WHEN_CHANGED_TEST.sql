CREATE VIEW dbo.WEAR_WHEN_CHANGED_TEST
AS
SELECT dbo.TipWear.Date_Time, dbo.TipWear.WeldingGunId, dbo.TipWear.BodysPerElectrode, dbo.TipWear.BodyRemaining, dbo.TipWear.ID, dbo.WeldingGun.ElectrodeNbr, 
                  dbo.Timer.Robot, dbo.WeldingGun.Name, LAG(dbo.TipWear.BodyRemaining, 1, '0') OVER (PARTITION BY dbo.TipWear.WeldingGunId
ORDER BY dbo.TipWear.Date_Time ASC) AS LastWear, (dbo.TipWear.BodyRemaining - LAG(dbo.TipWear.BodyRemaining, 1, '0') OVER (PARTITION BY 
dbo.TipWear.WeldingGunId
ORDER BY dbo.TipWear.Date_Time ASC)) AS DIFF_TEST
FROM     dbo.TipWear INNER JOIN
                  dbo.WeldingGun ON dbo.TipWear.WeldingGunId = dbo.WeldingGun.ID INNER JOIN
                  dbo.Timer ON dbo.WeldingGun.TimerID = dbo.Timer.ID
WHERE  (dbo.TipWear.Date_Time BETWEEN '2015/06/01' AND '2015/06/24')
GO
