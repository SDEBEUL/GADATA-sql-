CREATE VIEW LAUNCH.CheckDubbelPrograms
AS
SELECT
Q.Name,
Q.SpotName,
Q.WeldProgram
FROM(
SELECT        TOP (100) PERCENT LAUNCH.SpotTable.ID, dbo.Timer.Name, LAUNCH.SpotTable.SpotName, LAUNCH.SpotTable.WeldProgram, 
                         LAG(LAUNCH.SpotTable.WeldProgram, 1, 0) OVER (ORDER BY dbo.Timer.Name) AS prevData, LEAD(LAUNCH.SpotTable.WeldProgram, 1, 0) OVER (ORDER BY dbo.Timer.Name)
 AS nextData, LAG(dbo.Timer.Name, 1, 0) OVER (ORDER BY dbo.Timer.Name) AS prevTimer, LEAD(dbo.Timer.Name, 1, 0) OVER (ORDER BY dbo.Timer.Name) AS nextTimer
FROM            LAUNCH.SpotTable INNER JOIN
                         dbo.Timer ON LAUNCH.SpotTable.TimerID = dbo.Timer.ID
ORDER BY dbo.Timer.Name
) Q
WHERE( Q.Name=Q.nextTimer AND Q.WeldProgram=nextData) OR( Q.Name=Q.prevTimer AND Q.WeldProgram=PrevData)
GO
