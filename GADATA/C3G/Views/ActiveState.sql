




CREATE VIEW [C3G].[ActiveState]
AS
SELECT * FROM
(
SELECT  
              C.location AS 'Location',
			  C.controller_name AS 'Robotname',
			  y.controller_id,
			  c.enable_bit,
			  Y._timestamp AS 'Timestamp',
			  Y.c_timestamp AS 'C_timestamp',
			  ABS(DATEDIFF(minute,Y._timestamp,y.c_timestamp)) as 'ClockDiffMinute',
              '  Code: ' + CAST(Y.Sys_state AS varchar) +   '  SysState: ' + (GADATA.C3G.fn_decodeSysstate(y.Sys_state)) AS 'Logtekst',
			  y.sys_state,
			  GADATA.C3G.fn_robstate(y.sys_state) as 'robstate',
			  isnull(ROW_NUMBER() OVER (PARTITION BY y.controller_id ORDER BY y._timestamp Desc),1)  as 'rn'
FROM   GADATA.c3g.c_controller as C
--join the controller name
LEFT JOIN GADATA.C3G.rt_sys_event as Y  ON (Y.controller_id = C.id) 
) as x 
where x.rn = 1
GO
